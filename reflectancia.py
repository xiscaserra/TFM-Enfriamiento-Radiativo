# Filter solar data to fit the chosen wavelength range
df_solar_filtered = df_solar[
    (df_solar[wavelength_col_solar] >= min_wavelength_for_plot_nm) &
    (df_solar[wavelength_col_solar] <= max_wavelength_for_plot_nm)
].sort_values(by=wavelength_col_solar)

# Normalize Solar Spectrum Intensity
max_solar_intensity = df_solar_filtered[intensity_col_solar].max()
if max_solar_intensity > 0:
    df_solar_filtered['Normalized_Solar_Intensity'] = df_solar_filtered[intensity_col_solar] / max_solar_intensity * 100
else:
    df_solar_filtered['Normalized_Solar_Intensity'] = 0

# Plot Solar Spectrum as a shaded area
plt.fill_between(df_solar_filtered[wavelength_col_solar], 0, df_solar_filtered['Normalized_Solar_Intensity'], label='Espectro Solar (Normalizado)', color='salmon', alpha=0.3) # Increased alpha to 0.9

# --- Process Planck Distribution ---
# Generate wavelengths for Planck function directly in the plot range
wavelengths_for_planck_plot_nm = np.linspace(min_wavelength_for_plot_nm, max_wavelength_for_plot_nm, 500)

# Calculate Planck radiation for the given temperature (5900 K)
planck_temp = 5900
normalized_planck_radiance = planck_blackbody_radiation(wavelengths_for_planck_plot_nm, planck_temp)

# Scale the normalized Planck radiance (0-1) to 0-100 to compare with Reflectance (%)
sns.lineplot(x=wavelengths_for_planck_plot_nm, y=normalized_planck_radiance * 100, label=f'Planck {planck_temp} K (Normalizada)', color='black', linestyle='--')

# --- Plotting configurations ---
plt.title('Reflectancia para CA, NC y MCE frente a Longitud de Onda', fontsize=16)
plt.xlabel('Longitud de onda (nm)', fontsize=16)
plt.ylabel('Reflectancia (%)', fontsize=16)
plt.grid(True)
plt.ylim(0, 100)
