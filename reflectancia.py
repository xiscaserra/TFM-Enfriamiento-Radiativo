import numpy as np
import matplotlib.pyplot as plt

def planck_blackbody_radiation(wavelength_nm, temperature_k):
    """
    Calculates the spectral radiance of a blackbody according to Planck's Law
    and normalizes it to the maximum value.

    Args:
        wavelength_nm (float or np.ndarray): Wavelength in nanometers (nm).
        temperature_k (float): Temperature in Kelvin (K).

    Returns:
        float or np.ndarray: Normalized spectral radiance B(lambda, T) (scaled 0 to 1).
    """
    # Physical constants
    h = 6.62607015e-34  # Planck's constant (J*s)
    c = 2.99792458e8    # Speed of light (m/s)
    k = 1.380649e-23    # Boltzmann constant (J/K)

    # Convert wavelength from nanometers to meters
    wavelength_m = wavelength_nm * 1e-9

    # Avoid division by zero and very small numbers for exponential
    if temperature_k <= 0:
        return np.zeros_like(wavelength_m)

    # Calculate the terms in Planck's Law
    term1 = 2 * h * c**2
    term2 = wavelength_m**5
    term3_exponent = (h * c) / (wavelength_m * k * temperature_k)

    # Handle potential overflow/underflow in exponential
    exp_minus_1 = np.exp(term3_exponent) - 1

    radiance = np.zeros_like(wavelength_m, dtype=float)
    valid_indices = (wavelength_m > 0) & (exp_minus_1 > 1e-300)

    radiance[valid_indices] = (term1 / term2[valid_indices]) * (1 / exp_minus_1[valid_indices])

    # --- Normalization step ---
    max_radiance = np.max(radiance)
    if max_radiance > 0:
        normalized_radiance = radiance / max_radiance
    else:
        normalized_radiance = np.zeros_like(radiance)

    return normalized_radiance


