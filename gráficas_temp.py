def get_fabric_labels(file_name):
    """
    Parses the filename to extract fabric types for TS1 and TS2.
    Returns:
        tuple: (label_ts1_for_legend, label_ts2_for_legend, ts1_fabric_for_title, ts2_fabric_for_title)
    """
    fabric_map = {
        'L': 'lino',
        'P': 'poliéster',
        'A': 'algodón',
        'T': 'tencel',
        'S': 'sin nada',
        'B': 'negra'
    }

    # Initialize fabric names for title (plain names)
    ts1_fabric_for_title = ""
    ts2_fabric_for_title = ""

    # Extract the relevant part of the filename, e.g., 'L1P2' from 'datos_260504_1920_L1P2.csv'
    base_name = os.path.basename(file_name)
    parts = base_name.split('_')

    if len(parts) >= 4:
        fabric_codes_part = parts[-1].split('.')[0] # e.g., 'L1P2' or 'S1S2'

        # Iterate through the fabric_codes_part looking for '1' and '2'
        for k in range(len(fabric_codes_part)):
            if fabric_codes_part[k] == '1' and k > 0:
                ts1_code = fabric_codes_part[k-1]
                if ts1_code in fabric_map:
                    ts1_fabric_for_title = fabric_map[ts1_code] # Store plain fabric name
            elif fabric_codes_part[k] == '2' and k > 0:
                ts2_code = fabric_codes_part[k-1]
                if ts2_code in fabric_map:
                    ts2_fabric_for_title = fabric_map[ts2_code] # Store plain fabric name

    # Labels for legend (more descriptive, with TS1/TS2 prefix)
    label_ts1_for_legend = f"TS1 ({ts1_fabric_for_title})" if ts1_fabric_for_title else "TS1"
    label_ts2_for_legend = f"TS2 ({ts2_fabric_for_title})" if ts2_fabric_for_title else "TS2"

    return label_ts1_for_legend, label_ts2_for_legend, ts1_fabric_for_title, ts2_fabric_for_title


