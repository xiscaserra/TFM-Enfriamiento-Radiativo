def interpolate_temperature(df, target_datetime_str, temp_column='TMPA01_Avg'):
    """
    Interpola el valor de temperatura (temp_column) para una fecha y hora dadas.

    Args:
        df (pd.DataFrame): DataFrame que contiene las columnas 'DateTime' y temp_column.
        target_datetime_str (str): Fecha y hora para la cual interpolar, en formato string (ej. 'YYYY-MM-DD HH:MM:SS').
        temp_column (str): Nombre de la columna de temperatura a interpolar.

    Returns:
        float: Valor de temperatura interpolado.
        str: Mensaje de error si la fecha está fuera de rango o si no se puede interpolar.
    """
    target_dt = pd.to_datetime(target_datetime_str)

    # Asegurarse de que el DataFrame esté ordenado por 'DateTime'
    df_sorted = df.sort_values(by='DateTime').reset_index(drop=True)

    # Verificar si la fecha objetivo está fuera del rango del DataFrame
    if target_dt < df_sorted['DateTime'].min() or target_dt > df_sorted['DateTime'].max():
        return f"Fecha y hora '{target_datetime_str}' fuera del rango de datos disponible."

    # Encontrar el índice de la primera 'DateTime' mayor o igual a la target_dt
    idx_after = df_sorted['DateTime'].searchsorted(target_dt)

    # Manejar coincidencia exacta
    if idx_after < len(df_sorted) and df_sorted.loc[idx_after, 'DateTime'] == target_dt:
        return df_sorted.loc[idx_after, temp_column]

    # Si target_dt es anterior al primer registro, pero dentro del rango (ya comprobado arriba)
    # Esto implica que idx_after debería ser >= 1 para la interpolación.
    if idx_after == 0:
        # Si el punto está antes del primer dato, pero no fuera de rango, podríamos extrapolar o devolver el primer valor.
        # Para este caso, devolvemos el primer valor conocido.
        return df_sorted.loc[0, temp_column]

    # Obtener los dos puntos de datos circundantes
    point1 = df_sorted.loc[idx_after - 1]
    point2 = df_sorted.loc[idx_after]

    dt1 = point1['DateTime']
    dt2 = point2['DateTime']
    temp1 = point1[temp_column]
    temp2 = point2[temp_column]

    # Manejar valores NaN en la temperatura
    if pd.isna(temp1) or pd.isna(temp2):
        return f"No se puede interpolar: valores de '{temp_column}' nulos en los puntos cercanos."

    # Realizar interpolación lineal
    # Convertir objetos datetime a timestamps numéricos para el cálculo
    time_diff_total = (dt2 - dt1).total_seconds()
    time_diff_target = (target_dt - dt1).total_seconds()

    if time_diff_total == 0: # Evitar división por cero, aunque no debería ocurrir si los datetimes son distintos
        return temp1

    interpolated_temp = temp1 + (temp2 - temp1) * (time_diff_target / time_diff_total)
    return interpolated_temp

